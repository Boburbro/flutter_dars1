class Weather {
  final String id;
  final String main;
  final String desc;
  final double temp;
  final String city;
  final double maxT;
  final double minT;

  Weather({
    required this.id,
    required this.main,
    required this.desc,
    required this.temp,
    required this.city,
    required this.maxT,
    required this.minT,
  });
}
